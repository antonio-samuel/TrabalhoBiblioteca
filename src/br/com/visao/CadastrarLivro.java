    private void jBcadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBcadastrarActionPerformed
        LivroDAO dao = new LivroDAO();
        String nomeAutor = jTnomeautor.getText();
        Autor autor = new AutorDAO().buscarPorNome(nomeAutor);
        int idAutor;

if (autor == null) {
     Autor novoAutor = new Autor();
    novoAutor.setNome(nomeAutor);
    idAutor = new AutorDAO().inserirERetornarId(novoAutor);
} else {
    idAutor = autor.getId();
}
String nomeCategoria = jTcategoria.getText();
Categoria categoria = new CategoriaDAO().buscarPorNome(nomeCategoria);
int idCategoria;

if (categoria == null) {
    Categoria novaCategoria = new Categoria();
    novaCategoria.setNome(nomeCategoria);
    idCategoria = new CategoriaDAO().inserirERetornarId(novaCategoria);
} else {
    idCategoria = categoria.getId();
}


// Agora insere o livro normalmente
 Livro l = new Livro();
 l.setTitulo(jTnomelivro.getText());
 l.setSinopse(jTsinopse.getText());
 l.setValor(Double.parseDouble(jTpreco.getText()));
 l.setIdAutor(idAutor);
 l.setIdCategoria(idCategoria);

  
 try{
     dao.inserir(l);
    JOptionPane.showMessageDialog(this, "Livro cadastrado com sucesso!");
      this.dispose();

        // Abrir o menu principal
        new Principal().setVisible(true);
 }
  catch(Exception ex) {
            Logger.getLogger(LivroDAO.class.getName()).log(Level.SEVERE, null, ex);
    }//GEN-LAST:event_jBcadastrarActionPerformed
    }
